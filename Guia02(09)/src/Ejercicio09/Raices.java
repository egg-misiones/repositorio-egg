package Ejercicio09;

public class Raices {

    private double A;
    private double B;
    private double C;

    public Raices() {
    }

    public Raices(double A, double B, double C) {
        this.A = A;
        this.B = B;
        this.C = C;
    }

    public void setA(double A) {
        this.A = A;
    }

    public void setB(double B) {
        this.B = B;
    }

    public void setC(double C) {
        this.C = C;
    }

    public double getA() {
        return A;
    }

    public double getB() {
        return B;
    }

    public double getC() {
        return C;
    }
}
