package Ejercicio16;

public class Matematica {

    private double nro1;
    private double nro2;

    public Matematica() {
    }

    public Matematica(double nro1, double nro2) {
        this.nro1 = nro1;
        this.nro2 = nro2;
    }

    public void setNro1(double nro1) {
        this.nro1 = nro1;
    }

    public void setNro2(double nro2) {
        this.nro2 = nro2;
    }

    public double getNro1() {
        return nro1;
    }

    public double getNro2() {
        return nro2;
    }
}
